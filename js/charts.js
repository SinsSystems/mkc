function romanize(num) {
    if (!+num)
        return NaN;
    var digits = String(+num).split(""),
        key = ["", "C", "CC", "CCC", "CD", "D", "DC", "DCC", "DCCC", "CM",
            "", "X", "XX", "XXX", "XL", "L", "LX", "LXX", "LXXX", "XC",
            "", "I", "II", "III", "IV", "V", "VI", "VII", "VIII", "IX"],
        roman = "",
        i = 3;
    while (i--)
        roman = (key[+digits.pop() + (i * 10)] || "") + roman;
    return Array(+digits.join("") + 1).join("M") + roman;
}

function createSeatChart(ar, m, u) {
    ar.forEach((e, ind) => {
        var i = '';
        var b = 1;
        var r = romanize(ind + 1);
        var rowAlign = 'center';
        if(u){
            r = romanize(ind + 11);
        }
        if(m == 'l'){
            rowAlign = 'left';
        } else if (m== 'r'){
            rowAlign = 'right';
        }

        e.forEach(n => {
            i += '<div class="btn-group btn-group-toggle" data-toggle="buttons" style="margin:2px;">' +
                '<label class="btn btn-info btn-sm chartButton" id="' + n + '" style="width: 34px">' +
                '<input type="checkbox" checked="" autocomplete="off"> ' + b +
                '</label>' +
                '</div>';
            b++;
        });

        $('.seatChartsContainer').prepend('<div class="text-' + rowAlign + '"><div class="text-center" style="display: inline-block; width: 33px;"><small>' + r + '</small></div> ' + i + '</div>');

    });

    eventObject.ocupiedSeats.forEach(ss => {
        $('label#' + ss).removeClass('btn-info btn-success').addClass('btn-danger disabled').css('pointer-events', 'none');
    });

    $('.seatChartsContainer').fadeIn(500);

    getSelectedSeats();
}

